# Extend datasets to have 500 samples each
def extend_urls(url_list, target=500):
    result = []
    while len(result) < target:
        for url in url_list:
            if len(result) >= target:
                break
            # Slightly modify URLs to create variations
            variations = [
                url,
                url.replace("http://", "https://") if "http://" in url else url,
                url + "/index.html" if not url.endswith(".html") else url,
                url + "?id=" + str(random.randint(1000, 9999)),
            ]
            result.append(random.choice(variations))
    return result[:target]

legit_extended = extend_urls(legitimate_urls, 600)
phish_extended = extend_urls(phishing_urls, 600)

urls = legit_extended + phish_extended
labels = [0] * len(legit_extended) + [1] * len(phish_extended)

df = pd.DataFrame({"url": urls, "label": labels})
df = df.sample(frac=1, random_state=42).reset_index(drop=True)

